package pojos;

/**
 * Enumeration representing different medical specialties that a doctor can have.
 * Used to categorize doctors based on their field of expertise.
 */
public enum DoctorSpecialty {
    /**
     * Specialist in diagnosing and treating heart conditions.
     */
    CARDIOLOGIST,
    /**
     * Specialist in diagnosing and treating neurological disorders.
     */
    NEUROPHYSIOLOGIST,
    /**
     * Practitioner of general medicine and primary care.
     */
    GENERAL_MEDICINE
}
