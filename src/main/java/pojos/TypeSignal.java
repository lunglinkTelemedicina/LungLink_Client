package pojos;

/**
 * Represents the different types of biological signals that can be processed.
 * This enum is used to identify and categorize various signal measurements.
 */
public enum TypeSignal {
    /**
     * Electrocardiogram signal type, measuring electrical activity of the heart
     */
    ECG,
    /**
     * Electromyography signal type, measuring electrical activity produced by skeletal muscles
     */
    EMG
}
